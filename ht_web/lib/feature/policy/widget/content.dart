- لنشر إعلانات متعلقة بالموقع عبر البريد الإلكتروني.

- لإعلان خدمات جديدة متعلقة بالموقع عبر البريد الإلكتروني.

- استطلاع ردود الفعل الخاصة بك بخصوص الموقع.

- إسداء نصائح بخصوص التحديثات والإضافات الجديدة للدورات، وإعطاء معلومات عن المحتوى والنشاطات التي قد تكون ضمن نطاق ما يعجب الأعضاء بما فيها المنتجات أو الخدمات المقدمة من الطرف الثالث. لهذه الأسباب قد نقوم بمشاركة مثل هذه المعلومات مع أطراف ثالثة أخرى.

