خصوصية الأعضاء والزوار تعتبر مهمة جداً بالنسبة لنا، ونحن ملتزمون تماماً بالحفاظ على خصوصيتهم وأمانهم. نحن لا ننشر ولا نشارك أي معلومات شخصية عن زوارنا وأعضائنا إلا في حالات الطلب القانونية وما يتوافق مع القانون، وأيضاً لتطوير خدماتنا أو عند حمايتنا لحقوقنا. إن السبب وراء استقبالنا معلومات المستخدم هو تمكيننا من منح الخدمات عبر موقعنا لمستخدمينا النهائيين. تحتوي مثل هذه المعلومات، من ضمن ما تحتوي، على معلومات تمكننا من التعرف على عملائنا والتعامل معها وفقاً لأحكام القوانين ذات الصلة، مثل المعلومات المتعلقة بالعمر، والتي تجعلنا قادرين على تحديد مدى أهلية عميلنا للدخول في اتفاق ملزم قانونياً، والمعلومات المطلوبة لتمكين مستخدمينا من الدخول على موقعنا بصورة متكررة كاسم المستخدم وكلمة المرور، وكذلك عنوان البريد الإلكتروني والذي يتم استخدامه لإ

دارة حساب المستخدم، خاصةً في تلك الأحوال حين يتم نسيان أسماء الدخول وكلمات المرور، والمعلومات التي تمكننا من التعامل مع العميل؛ كوسيلة الدفع المفضلة، والمعلومات المطلوبة لتقديم الخدمات لعملائنا؛ كعنوان البريد الإلكتروني، والعنوان المادي، ورقم الهاتف المحمول، والتي تُستخدم جميعاً لإرسال الإيصالات والفواتير والمبالغ المالية. هذه المعلومات ستستخدم داخلياً من قبلنا لمعرفتك كعضو في الموقع. نحن نجمع ونستخدم المعلومات الشخصية التعريفية من الزوار والأعضاء للأغراض التالية:

