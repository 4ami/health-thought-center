### تعهد بضمان خصوصية المستفيدين

#### الالتزام بخصوصية المستفيد

الإقرار بالالتزام بضمان خصوصية بيانات المستفيد من خلال وجود سياسات وإجراءات واضحة بموقع مركز الفكر الصحي للتدريب

يقر مركز الفكر الصحي للتدريب بالتالي:

أن المركز يطبق سياسة فعالة لتحقيق خصوصية بيانات المستفيدين وحمايتها وضمان تخزينها وعدم الاطلاع عليها إلا من قبل المختصين وذوي الصلاحية وعدم نشرها ومشاركتها مع أي جهة أخرى أو أشخاص لا يحق لهم الاطلاع على هذه البيانات.

أن المركز يقوم بنشر وثيقة ضمان الخصوصية ونشرها على الموقع عبر رابط في موقعه الإلكتروني ليتمكن جميع المستخدمين من معرفة طريقة جمع البيانات وتخزينها ومن يملك صلاحية الاطلاع على هذه البيانات.

يوضح المركز سياسة عبر نشرها في صفحة اتفاقية استخدام الموقع تحت عنوان (سياسة الخصوصية).

أن مركز الفكر الصحي للتدريب يوضح عبر سياسة الخصوصية المنشورة على الموقع إنه لا يبيع أو يمنح أي أطراف خارجية المعلومات الشخصية للمستفيدين وقد تعهد المركز بذلك.

### أنظمة للتحقق من هوية المستفيد

موقع مركز الفكر الصحي يقتصر على تقديم دورات تطويرية وتأهيلية ولا تتضمن الدورات المقدمة عبر الموقع أية امتحانات أو اختبارات بنهاية الدورات التدريبية يقدم الموقع الدورات التطويرية عبر السماح للمستفيد بالاشتراك في الموقع ومشاهدة الدورة التطويرية بعد التحقق من هويته وإتمام التسجيل.

ويلتزم المركز بالتحقق من هوية المستفيد.

الآليات التي يستخدمها مركز الفكر الصحي للتدريب للتحقق من هوية المستفيد تتم عبر خطوتين:

الأولى: تسجيل الدخول في الموقع يكون عبر إرسال رسالة كود للبريد الإلكتروني للتحقق منه.

الثانية: يقوم المركز بإرسال بيانات المستفيد للمؤسسة العامة للتدريب التقني لتقوم بمطابقتها والتحقق من هوية المستفيد عبر استخدام الهوية الوطنية في حال طلب المستفيد الحصول على شهادة.

### للتواصل مع فريق الدعم الفني في حال تغيير رقم الجوال أو البريد الإلكتروني المسجل

#### زوار الموقع والمعلومات التعريفية غير الشخصية

يجمع مركز الفكر الصحي للتدريب معلومات تعريفية غير شخصية كنوع المتصفح والموقع الذي تمكنت منه الوصول إلى منصة مركز الفكر الصحي للتدريب ووقت وتاريخ الزيارة، لإعطائنا فهم أفضل لسلوك الزوار والأعضاء في التعامل مع منصة مركز الفكر الصحي كموقع. قد نقوم بنشر معلومات تعريفية غير شخصية لعرض ما يجذب انتباه الزوار فيما يخص استخدام الموقع. قد نقوم بجمع إحصاءات عن سلوك المستخدمين والزوار في تعاملهم المباشر مع الموقع. معلومات مثل الدورات الأكثر تسجيلا أو الأكثر نجاحا أو مراجعة إلخ... ستعرض للعامة مباشرة. ولكننا ملتزمون بعدم عرض أي معلومات شخصية. تُعد معلومات المستخدم أصل رئيسي في معظم الأعمال المبنية على الإنترنت. وكشركة، قد نكون عرضة دائماً لمعاملات تتعلق بأصولنا وأسهمنا العادية بما في ذلك الاندماج، الانقسام، الاكتساب من قِبل الغير. وتعد معلومات المستخدم شيء رئيسي في التقييمات التي تحدث قبل هذه الأعمال وأحد الأصول الرئيسية المنقولة فور أن تحدث تلك المعاملات. سنضمن أن خلفاءنا القانونيين يتعاملون مع معلومات المستخدم بنفس الطريقة التي نتعامل بها، واستمرارهم في تطوير وتنفيذ الوسائل لحمايتها بشكل أفضل.

