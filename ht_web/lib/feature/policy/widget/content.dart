يجب دائما الاعتراف بحقوقنا (وحقوق أي من المشاركين الذين يتم تحديدهم) في تأليف المحتوى المنشور على موقعنا.

يجب عدم استخدام أي جزء من المحتوى المنشور على موقعنا لأغراض تجارية دون الحصول على ترخيص بذلك منا أو من الجهات المُرَخِّصة لنا.

في حال قيامكم بطباعة، أو نسخ أو تحميل أي جزء من موقعنا بطريقة مخالفة لشروط الاستخدام هذه، فإن حقكم في استخدام موقعنا سيتوقف فوراً، ويكون لنا الخيار أن نطلب منكم إعادة أو إتلاف أي نسخ من المواد التي قمتم بالحصول عليها بهذه الطريقة.

### التزام إدارة المركز بحقوق الملكية الفكرية عند النشر:

تلتزم إدارة المركز بمبادئ حقوق الملكية الفكرية عند تصميم ورفع أي محتوى تدريبي على الموقع، مثل النصوص، أو الصور، أو البرامج، أو التصاميم، أو محتويات تدريبية، أو غيرها.

وتعود إلى مركز الفكر الصحي للتدريب الملكية الفكرية للمحتوى الرقمي المنشور على موقع المركز بما في ذلك المقالات والبرامج، والتصاميم، والصور، وغيرها.

### التزام المدربين بحقوق الملكية الفكرية عند عرض المحتويات التدريبية على موقع المركز:

يلتزم المدربون الذين يرغبون في رفع محتوياتهم التدريبية بمبادئ حقوق الملكية الفكرية والنشر. حيث يقوم المدرب بالتوقيع على نموذج تعهد وإقرار بأن المحتوى التدريبي المقدم في الدورات التدريبية يحترم فيه جميع قوانين الملكية الفكرية وحقوق النشر، والاستخدام، والتعديل، والمزج. كما يتعهد كذلك المدربون بالإشارة إلى المراجع العلمية والمصادر التي استفاد منها في إعداد المحتوى التدريبي، وفي حالة استخدام المدرب لمحتوى تدريبي لا يملك ملكيته الفكرية أو لا يذكر فيه المراجع والمصادر يتحمل المسؤولية النظامية عن ذلك.

### التزام المدرب عند رفع المحتوى التدريبي على موقع المركز:

يقر المدربون أن كل محتوى تدريبي رقمي يتم رفعه على موقع المركز من محاضرات مسجلة أو تكليفات أو كائنات تعليمية مختلفة لا بد أن يراعى فيها احترام حقوق الملكية الفكرية ومبادئ حقوق النشر، كما يقر المدربون أن جميع المحتويات التدريبية المرفوعة على الموقع تصبح تحت تصرف مركز الفكر الصحي، والتي يمكن لها أن يستخدمها لأغراض بحثية أو أكاديمية.

ومن ضوابط حقوق النشر التي تلتزم بها إدارة المركز والموضحة لجميع المستفيدين ما يلي:

1. يمنع نشر أي محتوى تدريبي يتعرض لأشخاص او مؤسسات يظهرهم فيه بطريقة سيئة.

2. يمنع التعدي بنشر أو اقتباس محتوى ذو حقوق فكرية بدون تصريح أو موافقة.

3. يمنع نشر أي محتوى مخالف للتعاليم الدينية او الأخلاقية السائدة في المجتمع.

4. يمنع نشر أي محتوى يؤثر على الوحدة الوطنية (عنصرية، طائفية، قبلية ....).

5. يمنع النشر أو الترويج لأي محتوى أو منتج ذو أهداف تجارية.

### تعهد بالالتزام ونشر مبادئ حقوق الملكية الفكرية وحقوق النشر:

1. أن المحتوى المقدم للدورات التدريبية في كافة الدورات هو ملك للمركز ملكية حصرية.

2. أن الدورات التدريبية المقدمة يمتلك الموقع حقوقها وحقوق نشرها عبر شرائها من المدربين المسجلين للدورات التدريبية.

3. يتعهد المركز ويقر أن المحتوى المقدم للدورات لا يخالف حقوق النشر ولا يتعدى على أي حقوق لأي طرف أو جهة.

4. كما يتعهد المركز بمبادئ حماية حقوق الملكية الفكرية وحقوق نشر المحتوى الرقمي في الموقع.

5. يوضح المركز

 سياسة الملكية الفكرية لجميع المستفيدين من الدورات عبر نشرها في صفحة اتفاقية استخدام الموقع تحت عنوان (حقوق الملكية الفكرية).

6. يتعهد المركز بفحص جميع الدورات التدريبية التطويرية الإلكترونية المقدمة للتأكد من خلوها من انتهاكات الملكية الفكرية.

### سياسة الحضور للدورات التدريبية عن بعد

تشير هذه السياسة إلى أن الحضور الافتراضي يعادل الحضور الاعتيادي، ومن الأحكام العامة المتعلقة بها:

1. على المتدرب أن يتقدم بطلب التسجيل في الدورات التدريبية عن طريق المنصة الإلكترونية.

2. على المتدرب دفع رسوم التدريب بالدفع عبر جهات الدفع المعتمدة بالمنصة.

3. عند الرغبة في إلغاء طلب التسجيل يجب التقدم بطلب إلى المنصة قبل (24) ساعة من انعقاد الدورة على الأقل من انعقاد الدورة وللمنصة قبول الاعتذار أو رفضه.

4. تحتفظ منصة مركز الفكر الصحي للتدريب بحق إلغاء أي دورة تدريبية أو تغيير موعد انعقادها، ويبلغ المتدربون بذلك.

5. الأحكام والشروط قابلة للتعديل دون إشعار مسبق.

6. إن حضور الدورات افتراضياً يعادل حضور المتدرب العادي.

7. على المتدرب حضور ما لا يقل عن (75%) من الساعات التدريبية المخصصة للدورة المسجّل فيها.

8. عند غياب المتدرب عن حضور الدورة يفقد حقه بالمطالبة باسترداد الرسوم ما لم يقدم عذر رسمي، وتحتفظ منصة مركز الفكر الصحي للتدريب بحق قبول العذر أو رفضه.

### تعهد:

