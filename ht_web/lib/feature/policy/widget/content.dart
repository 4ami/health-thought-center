### الخطة التدريبية للمدربين لإتمام التدريب الإلكتروني

مركز الفكر الصحي، إحدى المؤسسات الوطنية الرائدة والتي تعمل بشكل تخصصي في مجال التدريب وتطوير الأفراد في المجالات المختلفة، وذلك من خلال طاقم مميز من المدربين والفنيين ذوي الخبرة في هذا المجال، فقد تم توفير جميع دعائم نظام التدريب الإلكتروني داخل المركز علاوة على تدريب المدربين على المحاور الثلاثة (استخدام التقنية، تطوير المحتوى الإلكتروني، مهارات التدريس) وتشمل الآتي:

#### أولاً: استخدام التقنية

يتم تدريب المدربين ولمدة 10 ساعات على اكتساب المفاهيم الأساسية المتعلقة بالتقنية والتمكن من استخدام تطبيقاتها في التعليم حيث يكون المدرب في نهاية التدريب قادراً على:

- التخطيط والتصميم الفعال للتعليم عن بعد باستخدام الوسائل التقنية.

- استخدام الوسائل التقنية في عملية التقويم من خلال استخدام أساليب مختلفة للتقويم الإلكتروني.

- استخدام الوسائل التقنية لتطوير أدائه وممارسته المهنية.

- استخدام الأجهزة اللاسلكية المتنقلة الصغيرة والمحمولة يدوياً مثل الهواتف المتنقلة، والمساعدات الرقمية الشخصية، والهواتف الذكية، والحاسبات الشخصية الصغيرة لتحقيق المرونة والتفاعل في عمليتي التدريس والتعلم في أي وقت وفي أي مكان.

- الدخول إلى المنصة واستخدام النظم المستخدمة بالعملية التعليمية.

- التأكد من امتلاك الأدوات اللازمة لإيصال التدريب الإلكتروني بكل كفاءة.

- المقدرة على التعامل مع المادة العلمية تقنياً وأنظمة الحضور وأنظمة الاختبارات.

- الاستخدام الآمن لمصادر التقنية.

#### ثانياً: تطوير المحتوى الإلكتروني

يتم تدريب المدربين ولمدة 10 ساعات على تصميم وتطوير المحتوى الإلكتروني باستخدام الحاسوب وتطبيقاته المختلفة، ونشره وعرضه للطلاب وإمكانية الاختيار من بين بدائل متعددة لمصادر التعلم والوسائط التفاعلية حسب احتياجات كل متدرب، واستخدام أنظمة إدارة التعلم، وأنظمة إدارة المحتوى التعليمي بالإضافة إلى تعليم كافة خدمات ووسائل Google في ذلك.

