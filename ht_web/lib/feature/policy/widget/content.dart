### آلية لفحص أعمال المتدربين ومنع الغش وانتحال الهوية:

#### أولا. التقنيات المستخدمة لمنع الغش:

1. تعهد وإقرار المتدرب على أنظمة وعقوبات ضبط حالات الغش وانتحال الهوية قبل بدء إجراء الاختبار يرسلها له المدرب عبر الرابط.

2. تصميم اختبارات تجمع ما بين العشوائية والعدالة، بحيث تتلقى الأسئلة من بنوك ومخازن معدة مسبقاً لتغطي مخرجات التعلم، وطريقة الإجابة عنها لا تعتمد على الحفظ والاسترجاع بل على الفهم والاستيعاب.

3. تفرد كل نموذج اختبار رقمي عن الآخر واعتماد أسئلة جديدة غير محلولة مسبقاً وغير متوفر إجابتها في الكتب والمراجع.

4. منع تصوير الأسئلة أو التواصل مع الآخرين أو الاستعانة بمصادر غير مسموح بها.

5. قد يطلب من المتدرب تشغيل كاميرا الفيديو الخاصة به للتحقق من هوية المتدرب وقد يطلب منه إبراز الهوية الشخصية.

6. قد يطلب تمرير الكاميرا في أنحاء الغرفة للتأكد من عدم وجود أشخاص آخرين في الغرفة وعدم وجود أي مواد مساندة أو جوال أو ما شابه.

7. حملة توعية شاملة للطلبة والطاقم التدريبي بأنظمة وعقوبات ضبط حالات الغش وانتحال الهوية.

8. استخدام الحلول التقنية كمتصفح يمنع فتح أي برامج أخرى على سطح المكتب أثناء إجراء الاختبار ويمنع نسخ الامتحان.

9. ضبط أية أحداث تخالف السلوكيات المسموح بها يتم مراجعتها من قبل الطاقم التدريبي وخبراء النزاهة الأكاديمية وخبراء التكنولوجيا لضمان جودة العملية التقييمية.

10. عرض شاشة تعليمات إجراء الاختبارات أمام المتدرب أثناء إجراء الاختبار وتتضمن جميع أنظمة وعقوبات الغش وانتحال الهوية أثناء الاختبار.

#### ثانيا. المخالفات:

1. انتحال صفة شخص آخر بهدف التقدم بدلاً عنه لدخول الاختبار أو الاتفاق مع آخر للتقدم بدلاً منه للدخول للاختبار.

2. ارتكاب أي سلوك من شأنه التأثير سلباً على سير الاختبارات سواء كان الهدف منه الغش في الاختبار أو لأي سبب آخر.

3. الحصول أو محاولة الحصول على الاختبار قبل بدأة بأي طريقة.

4. ارتكاب أي حالة من حالات الغش.

5. عدم التقيد بتعليمات الاختبار أو المشرف عن الاختبار المتعلقة بتنظيم إجراء الاختبار مثل رفض فتح الكاميرا الشخصية للمتقدم للاختبار أو عدم التوقف عن سلوك مخالف تم التنبيه عليه من قبل المشرف على الاختبار.

#### ثالثا. العقوبات:

1. في حالة ارتكاب أي مخالفة لقواعد الاختبار فانه توقع على المخالف واحدة أو أكثر من العقوبات التالية:

   - توجيه إنذار بحرمانه من دخول الاختبار.

   - اعتبار المتقدم راسب في بعض المواد أو كل الاختبار.

   - إلغاء نتائج المتقدم في الاختبارات السابقة.

   - الحرمان من دخول الاختبارات لمدة دورة واحدة أو أكثر بحيث لا تزيد مدة الحرمان عن سنة كحد أقصى.

#### رابعا. إجراء التحقيق وإيقاع العقوبات:

تتبع في التحقيق وإيقاع العقوبات على المخالف الإجراءات التالية:

- إذا كانت المخالفة تتمثل بارتكاب حالة من حالات الغش أو تتعلق بتنظيم إجراء الاختبار يقوم المشرف على الاختبار بتحرير محضر إثبات حالة يبين فيها تفاصيلها ويطلب من المخالف أو المخالفين ومن شهد الواقعة كتابة إفادتهم بشأنها لترفع للجنة الاختبارات ويوقع على المحضر من قبل المشرف على الاختبار وفريق الإشراف على الاختبارات ويتولى فريق الإشراف على الاختبارات رفع المحضر مع إفادات المعنيين إلى رئيس لجنة الاختبارات خلال أربعة أيام عمل من تاريخ وقوع المخالفة.

- تقوم لجنة الاختبارات بالتحقيق في المخالفة وتعد لجنة التحقيق محضر تحقيق وترفع اللجنة محضر التحقيق وتوصياتها إلى لجنة الاختبارات بشأن مدى ثبوت المخالفة والعقوبة المناسبة.

- في حالة ثبوت المخالفة تقوم لجنة الاختبارات برفع ما توصلت إليه لجنة التحقيق وتوصياتها مقرونة بإثباتها الحالة إلى الإدارة المعنية.

- تصدر قرارات إيقاع العقوبة من الإدارة المعنية وإذا قرار الإدارة المعنية

 نهائي يتم تبليغ من صدرت بحقه العقوبة بالمخالفة المرتكبة والعقوبة الصادرة بحقه.

### إقرار المتدرب على أنظمة وعقوبات ضبط حالات الغش وانتحال الهوية

توفر مركز الفكر الصحي للتدريب أنظمة تعليم إلكتروني تدعم مختلف أنواع الأجهزة باختلاف نظم تشغيلها. متوافقة مع جميع شاشات الأجهزة بما فيها الأجهزة اللوحية والهواتف الذكية مع توفر نظام تقني دقيق وقواعد بيانات موثقة ودقيقة وشبكات اتصال عالية الجودة. من جهة أخرى تم توفير نظام حماية لكل مكونات العمل التقني بشكل يحفظ ويحمي كامل المحتويات والبيانات. كما يتوفر فريق دعم فني عالي المستوى وعلى مدار الساعة ومن خلال كافة وسائل الاتصال وذلك لتقديم الدعم اللحظي والحلول والبدائل التقنية. مع مراعاة أحدث البرمجيات والمنصات التفاعلية حيث يمكن للمتدرب أو المستفيد التفاعل من خلال كافة الوسائل مثل أجهزة الحاسب والهواتف الذكية.

### تعهد باستخدام أنظمة التعليم الإلكتروني

كما يتعهد المركز بما يلي:

- التأكد من حداثة المحتوى الإلكتروني المقدم في البرامج، وعدم مخالفة القوانين والسياسات الوطنية.

- الالتزام ونشر مبادئ حقوق الملكية الفكرية وحقوق النشر.

- استخدام أنظمة تعليم إلكتروني تدعم مختلف أنواع الأجهزة باختلاف أنظمة تشغيلها ومتوافقة مع جميع الأجهزة بما فيها الأجهزة اللوحية والهواتف الذكية.

- توفير خدمة التنبيهات في أنظمة إدارة التعلم لتنبيه المتدربين بالمتطلبات بشكل مستمر لتحسين استمرارية التدريب ومتابعة المهمات والواجبات داخل البرنامج التدريبي التي تزيد مدتها عن شهر.

- توفير خاصية البحث عن المحتوى الرقمي في النظام.

- توفير الدورات التدريبية والتأهيلية والأدلة الإرشادية الإلكترونية للبرامج المستخدمة في بيئات التدريب الإلكتروني للمتدربين.

- تقديم المساعدة الفنية والتعليمية للمدربين والمتدربين طوال مدة تنفيذ التدريب.

- الالتزام بحصول المدربين على الشهادات الاحترافية في مجالات التعليم الإلكتروني.

### دليل نظام التنبيهات

1. يقدم الموقع الدورات التطويرية عبر السماح للمستفيد بالاشتراك في الموقع وتحميل الدورة التطويرية بعد التحقق من هويته وإتمام التسجيل.

2. يوفر الموقع نظام تنبيهات إلكتروني لتنبيه المتعلم على المهام المطلوبة بشكل مستمر لتحسين استمرارية التعلم ومتابعة المهمات والواجبات داخل المقرر الدراسي.

3. التنبيهات والروابط تظهر للمستفيدين.

4. يستخدم نظام التنبيه لتنبيه المستفيدين عن الإخطارات التالية على سبيل المثال لا الحصر:

   - المدة المتبقية للدعم الفني للدورة / انعقاد ورش العمل /... وغيرها.

5. يوفر نظام التنبيهات إخطارات تتناسب مع الغرض من الرسالة.

6. الإخطارات تظهر بمجرد الضغط على رابط التنبيهات.

### خطة تدريبية للمدرب على المحاور الثلاثة (استخدام التقنية، تطوير المحتوى الإلكتروني، مهارات التدريس)

#### استخدام التقنية:

يتم تدريب المدربين ولمدة 15 ساعة تدريبية على اكتساب المفاهيم الأساسية المتعلقة بالتقنية والتعرف على تطبيقاتها في التعليم حيث يتوقع من المدرب في نهاية التدريب أن يكون قادراً على:

1. التخطيط والتصميم الفعال للتعليم المدعوم بالتقنية.

2. استخدام التقنية في تيسير عملية التقويم.

3. استخدام التقنية لتطوير أدائه وممارسته المهنية.

4. الدخول إلى المنصة واستخدام النظم الوسيطة المستخدمة بالعملية التعليمية.

5. التأكد من امتلاك الأدوات اللازمة لإيصال التدريب الإلكتروني بكل كفاءة.

6. المقدرة على التعامل مع المادة العلمية تقنياً وأنظمة الحضور وأنظمة الاختبارات.

7. الاستخدام الآمن لمصادر التقنية.

#### تطوير المحتوى الإلكتروني:

يتم تدريب المدربين ولمدة 15 ساعة تدريبية على تصميم وتطوير المحتوى الإلكتروني باستخدام الكمبيوتر وتطبيقاته المختلفة، ومن ثم نشره وعرضه للمتدربين وإمكانية الاختيار من بين بدائل متعددة لمصادر التعلم والوسائط التفاعلية حسب احتياجات كل متدرب سواء أكان ذلك بصورة متزامنة (Synchronous) أو غير متزامنة (Asynchronous) واستخدام أنظمة إدارة التعلم، وأنظمة إدارة المحتوى التعليمي.

#### مهارات التدريب:

المدربون لدينا يحصلون على تدريب لاكتساب مهارات التدريب على المراحل التالية:

- مرحلة التقنية: وتشمل:

  - إتقان المهارات الأساسية للحاسب.

  - استخدام أنظمة إدارة المحتوى التعليمي.

  - استخدام منصات الوسائط الاجتماعية عبر الإنترنت في البيئات الأكاديمية.

- مرحلة التصميم: وتشمل:

  - وضع وصف متكامل للمادة.

  - إتقان مهارة تصميم المحتوى التعليمي الإلكتروني.

  - إتقان المادة واستخدام أدوات التعلم الإلكترونية المناسبة.

  - إتقان مهارة تنويع أساليب التدريب وتشجيع العمل ضمن الفريق والبحث عن الإجابات.

  - تصميم المادة وفقاً لمتطلبات الجهة المشرفة.

  - تحديث المحتوى التعليمي الإلكتروني مع الأخذ بعين الاعتبار الفروقات الفردية بين المتدربين.

- مرحلة النظام الإداري: وتشمل:

  - الحرص على متابعة الحضور الإلكتروني.

  - ضمان تفاعل المتدربين من خلال إشراكهم بالعملية التعليمية.

  - متابعة المتدربين وإنجازهم وتطور فهمهم للمادة.

  - التقييم المستمر والعادل.

### المساعدة الفنية والتعليمية

#### قنوات الدعم الفني

حرصاً من مركز الفكر الصحي للتدريب على تحقيق الأهداف التدريبية والتطويرية، فقد تم توفير آليات اتصال تقنية للتواصل المدربين والمتدربين مع فريق الدعم الفني للحصول على الإرشادات 
والحلول على مدار الساعة.

تقديم خدمات الدعم الفني في مركز الفكر الصحي للتدريب يخدم مجموعة متنوعة من المستفيدين، كل منهم يستفيد من هذه الخدمات بطرق مختلفة. إليك قائمة بالمستفيدين الرئيسيين:

1. المتدربون

	•	الدعم الفني: يساعد المتدربين في حل المشكلات التقنية مثل صعوبة الوصول إلى المحتوى الإلكتروني، أو مشكلات في الاتصال بالمنصة، أو الحاجة إلى مساعدة في استخدام أدوات معينة.

	•	المساعدة الأكاديمية: قد يحتاج المتدربون إلى توجيه أو إرشادات حول كيفية استخدام الموارد التعليمية بشكل فعال، أو كيفية إكمال الواجبات والمشاريع.

2. المدربون والمشرفون

	•	إعداد المحتوى: يوفر الدعم الفني للمدربين والمشرفين المساعدة في تحميل وتنظيم المحتوى التعليمي على المنصة، وضمان جودته.

	•	حل المشكلات التقنية: المساعدة في حل أي مشاكل تقنية يواجهها المدربون أثناء استخدام المنصة، مما يضمن تقديم تعليم سلس وفعال.

3. إدارة المركز

	•	تحسين العمليات: تساعد خدمات الدعم الفني الإدارة في فهم التحديات التقنية التي يواجهها المتدربون والمدربون، مما يمكنهم من تحسين البنية التحتية التعليمية والخدمات المقدمة.

	•	ضمان الجودة: من خلال الدعم الفني، يمكن للإدارة مراقبة جودة الخدمات المقدمة وضمان رضا المستفيدين.

خدمات الدعم الفني في مركز الفكر الصحي للتدريب تشمل مجموعة واسعة من الخدمات التي تهدف إلى دعم المتدربين، والمدربين، والإدارة، والموظفين الآخرين في استخدام المنصة التعليمية والأدوات التقنية المرتبطة بها. إليك بعض الخدمات الرئيسية التي يمكن أن تكون مشمولة بالدعم الفني:

1. الدعم التقني

	•	مساعدة في تسجيل الدخول: مساعدة المستخدمين في مشاكل تسجيل الدخول مثل استعادة كلمة المرور أو حل مشكلات الحساب المقفل.

	•	حل المشكلات الفنية: تقديم الدعم لحل مشكلات مثل عدم تحميل الصفحات، أو مشاكل في تشغيل الفيديوهات التعليمية، أو الأعطال التقنية الأخرى.

	•	التحديثات والصيانة: دعم في تطبيق التحديثات المطلوبة للبرامج والمنصة، وإدارة الصيانة الروتينية لضمان الأداء السلس للنظام.

2. التدريب والتوجيه

	•	إرشادات الاستخدام: تقديم إرشادات حول كيفية استخدام المنصة والأدوات المختلفة، مثل تحميل المواد، والاشتراك في الدورات، والمشاركة في المناقشات.

	•	التدريب على الأدوات التفاعلية: دعم المستخدمين في كيفية استخدام الأدوات التفاعلية مثل الاستطلاعات، والاختبارات الإلكترونية، وغرف الدردشة.

3. الدعم الأكاديمي والإداري

	•	إدارة المحتوى: مساعدة المدربين في تحميل وتنظيم المواد التعليمية، وتنسيق الفصول الدراسية الافتراضية.

	•	إعداد التقارير: دعم في استخراج التقارير الأكاديمية والتقدم الدراسي للمتدربين.

4. الدعم المتعلق بالأمان

	•	تأمين البيانات: إرشادات حول كيفية تأمين الحسابات الشخصية والمعلومات الحساسة.

	•	الحماية من الفيروسات والبرامج الضارة: توجيه المستخدمين نحو كيفية حماية أجهزتهم وبرامجهم من الفيروسات والبرامج الضارة.

5. الدعم عبر وسائل متعددة

	•	الدعم عبر الهاتف والبريد الإلكتروني: توفير خطوط اتصال لدعم المستخدمين عبر الهاتف والبريد الإلكتروني.

	•	الدردشة المباشرة: تقديم خدمات الدعم الفوري عبر الدردشة المباشرة على الموقع الإلكتروني أو المنصة.

	•	الأسئلة الشائعة والدعم الذاتي: توفير قاعدة بيانات شاملة للأسئلة الشائعة وإجاباتها لمساعدة المستخدمين في حل المشكلات البسيطة بأنفسهم.

6. التطوير والتحسين المستمر

	•	جمع الملاحظات: استقبال ملاحظات المستخدمين حول تجربة استخدام المنصة والخدمات المقدمة، والعمل على تحسينها باستمرار.

	•	اختبار الأداء: القيام باختبارات دورية للأداء لضمان أن النظام يعمل بكفاءة وأن جميع المستخدمين يمكنهم الوصول إلى الموارد بدون مشاكل

الوقت المتوقع للرد

في حال كان الدعم عن طريق الواتساب سواء من مدرب أو متدرب يتم الرد عليه خلال فترة زمنية قصيرة. عادة ما يتم الرد خلال دقائق عبر المحادثة. البريد الإلكتروني قد يستغرق 30 دقيقة لاستلام البريد ومن ثم الرد عليه.
تصعيد الطلب في حال عدم الرد في المدة المحددة

الهدف: تهدف هذه السياسة إلى ضمان استجابة فعّالة وفورية لجميع الطلبات والاستفسارات التقنية، وذلك من خلال تحديد إجراءات واضحة لتصعيد الطلبات في حال عدم الرد في المدة المحددة.

الإجراء:

مرحلة الرصد: يتم مراقبة الطلبات المستلمة بشكل دوري لضمان الالتزام بالوقت المحدد للرد.-

تحديد المدة: حديد فترة زمنية محددة للرد على الطلبات (على سبيل المثال، 48 ساعة عمل)

التنبيه: في حالة اقتراب انتهاء المدة المحددة وعدم الرد، يتم إرسال تنبيه إلى الفريق المعني بالدعم الفني.

تصعيد الطلب: ذا لم يتم الرد في المدة المحددة، يتم تصعيد الطلب إلى الفرق العليا المختصة.

تحليل السبب: يتم تحليل الأسباب التي أدت إلى عدم الرد في الوقت المناسب واتخاذ الإجراءات اللازمة لتجنب تكرارها.

